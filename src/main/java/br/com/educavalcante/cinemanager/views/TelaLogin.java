    private void signUpButtonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        String nome = nameField.getText().trim();
        String email = createEmailField.getText().trim();
        String senha = new String(createPasswordField.getPassword()).trim();
        
        Usuario user = usuarioController.criarConta(nome, email, senha, Role.CLIENTE);
        
        if(user != null) {
            FilmeDAO filmeDAO = new FilmeDAO();
            FilmeController filmeController = new FilmeController(filmeDAO);
            
            if(user.getRole() == Role.ADMIN) {
                new TelaAdmin(filmeController).setVisible(true);
            } else {
                new TelaCliente(filmeController, user).setVisible(true);
            }
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao criar conta.", "Erro de cadastro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_signUpButtonActionPerformed

