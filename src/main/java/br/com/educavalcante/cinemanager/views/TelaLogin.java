    private void loginButtonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String email = emailField.getText().trim();
        String senhaPlana = new String(passwordField.getPassword()).trim();

        Usuario user = usuarioController.entrar(email, senhaPlana);

        if(user != null) {
            FilmeDAO filmeDAO = new FilmeDAO();
            FilmeController filmeController = new FilmeController(filmeDAO);
            
            if(user.getRole() == Role.ADMIN) {
                new TelaAdmin(filmeController).setVisible(true);
            } else {
                new TelaCliente(filmeController, user).setVisible(true);
            }
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "E-mail ou senha incorretos.", "Erro de login", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

